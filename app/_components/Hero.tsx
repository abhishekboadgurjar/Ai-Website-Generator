"use client"

import { Button } from '@/components/ui/button'
import { UserDetailContext } from '@/context/UserDetailContext';
import { SignInButton, useAuth, useUser } from '@clerk/nextjs';
import axios from 'axios';
import { ArrowUp, HomeIcon, ImagePlus, Key, LayoutDashboard, Loader2Icon, User } from 'lucide-react'
import { useRouter } from 'next/navigation';
import { useContext, useState } from 'react'
import { toast } from 'sonner';
import { v4 as uuidv4 } from 'uuid';

const suggestion = [
  {
    label: 'Dashboard',
    prompt: 'Create a analytics dashboard to track customers and revenues ',
    icon: LayoutDashboard
  },
  {
    label: 'Signup Form',
    prompt: 'Create a user signup form with name,email and password fields',
    icon: Key
  },
  {
    label: 'Hero Page',
    prompt: 'Create a modern header and centered hero section for a landing page',
    icon: HomeIcon
  },
  {
    label: 'User Profile Card',
    prompt: 'Create a modern user profile card component for a social media app',
    icon: User
  }
]



function Hero() {

  const [userInput, setUserInput] = useState<string>('')
  const { user } = useUser();
  const router = useRouter();
  const [loading, setLoading] = useState<boolean>(false)
  const { has } = useAuth()
  const { userDetail, setUserDetail } = useContext(UserDetailContext)
  const hasUnlimitedAccess = has && has({ plan: 'unlimited' })

  const CreateNewProject = async () => {


    if (!hasUnlimitedAccess && userDetail?.credits! <= 0) {
      toast.error('You have no credits Please upgrade to unlimited plan')
      return;
    }

    setLoading(true)
    const projectId = uuidv4();
    const frameId = generateRandomFrameNumber();
    const messages = [
      {
        role: 'user',
        content: userInput
      }
    ]
    try {
      const result = await axios.post('/api/projects', {
        projectId: projectId,
        frameId: frameId,
        messages: messages,
        credits: userDetail?.credits
      });
      console.log(result);
      toast.success('Project created successfully');
      router.push(`/playground/${projectId}?frameId=${frameId}`)
      setUserDetail((prev: any) => ({
        ...prev,
        credits: prev.credits! - 1
      }))
      setLoading(false)
    }
    catch (e) {
      toast.error('Something went wrong');
      console.log(e);
      setLoading(false)
    }
  }


  return (
    <div className='flex flex-col items-center h-[80vh] justify-center text-center'>
      <h2 className='font-bold text-6xl'>What should we Design?</h2>
      <p className='mt-2 text-xl text-gray-500'>Genearte, Edit and Explore design with AI</p>

      <div className='w-full max-w-2xl p-5 border mt-5 rounded-2xl'>
        <textarea
          placeholder='Describe your page design'
          value={userInput}
          onChange={(e) => setUserInput(e.target.value)}
          className='w-full h-24 focus:outline-none focus:ring-0 resize-none'
        />
        <div className='flex justify-between items-center'>
          <Button variant='ghost'>
            <ImagePlus />
          </Button>
          {!user ? (
            <SignInButton mode='modal' fallbackRedirectUrl={'/workspace'}>
              <Button disabled={!userInput}>
                <ArrowUp />
              </Button>
            </SignInButton>
          ) : (
            <Button disabled={!userInput || loading} onClick={CreateNewProject}>
              {loading ? <Loader2Icon className='mr-2 animate-spin' /> : <ArrowUp />}
            </Button>
          )}
        </div>
      </div>

      <div className='mt-4 flex gap-3 flex-wrap justify-center'>
        {suggestion.map((item, index) => {
          const Icon = item.icon
          return (
            <Button
              key={index}
              variant='outline'
              onClick={() => setUserInput(item.prompt)}
            >
              <Icon className='mr-2' />
              {item.label}
            </Button>
          )
        })}
      </div>

    </div>
  )
}

export default Hero;



const generateRandomFrameNumber = () => {
  const num = Math.floor(Math.random() * 10000);
  return num;
}